<script>
    const config = {
        type: 'carousel',
        autoplay: 3000,
        gap: 0,
        perView: 1
    }
    const glide = new Glide('.hero-glide', config)

    const current = document.getElementById('current');
    const total = document.getElementById('total');

    // Кол-во слайдов считаем автоматически
    const slidesCount = document.querySelectorAll('.hero-glide .glide__slide').length;
    total.textContent = slidesCount.toString().padStart(2, '0');

    glide.on(['mount.after', 'run'], function () {
        let index = glide.index + 1;
        current.textContent = index.toString().padStart(2, '0');
    });

    glide.mount();
</script>
